#include <bits/stdc++.h>
using namespace std;
typedef long long ll;
ll n, sum, k = 1;
int main(){
	scanf("%lld", &n);//按照要求读入 
	sum = n;//给sum赋值 
	for (ll i = 1; i <= n; i++){
		//行数循环 
		for (ll j = 1; j <= sum; j++){
			//列数循环 
			if (k<10){
				//小于10输出前导0 
				printf("0");
				printf("%lld", k);
			}
			else{
				//否则直接输出 
				printf("%lld", k);
			}
			k++;
			//次数别忘加 
		}
		printf("\n");
		//换行 
		sum--;
	}
	return 0;
}